# PADRÃO ANSI
# O código 033 é o que funciona melhor com o Pyhton
# Após o colchete "[", o primeiro código digitado representa o style
# Após o código do sytle, vem o código que representa o text
# E por fim, após o código do text, vem o código do back e seguida a letra "m"
# \033[0;33;44m

# Style temos os seguinte códigos:
# 0 - none
# 1 - bold
# 4 - inderline
# 7 - negative

# Text
# 30 - branco
# 31 - vermelho
# 32 - verde
# 33 - amarelo
# 34 - azul
# 35 - roxo
# 36 - verde-água
# 37 - cinza

# Back
# Segue a mesma ordem de cores para o Text, porém do código 40 ao 47
